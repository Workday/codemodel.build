import build.base.telemetry.TelemetryRecorder;
import build.codemodel.foundation.CodeModel;
import build.codemodel.framework.compiler.Compiler;
import build.codemodel.jdk.JDKCodeModel;
import jakarta.inject.Singleton;

import java.io.IOException;
import java.io.UncheckedIOException;
import java.nio.file.Files;
import java.nio.file.Path;
import java.util.Comparator;
import java.util.List;
import java.util.stream.Collectors;

/**
 * A {@link Compiler} that reads the {@link BindingGraphTrait} from the {@link JDKCodeModel} and
 * emits a human-readable wiring report. When the trait is absent (i.e. {@link Context#snapshot}
 * was never called) this compiler is a no-op.
 *
 * <p>The report groups bindings by scope, lists their direct dependencies and dependents, and
 * flags scope violations. It is written to the {@link Path} supplied at construction time.
 *
 * <p>For projects that do not use the {@code codemodel-framework} annotation-processor pipeline,
 * the equivalent output is produced directly by {@link Context#snapshot}.
 *
 * @author reed.vonredwitz
 * @see BindingGraphTrait
 * @see Context#snapshot
 * @since Apr-2026
 */
public final class WiringReportCompiler
    implements Compiler<JDKCodeModel> {

    /**
     * The path to which the wiring report is written.
     */
    private final Path outputPath;

    /**
     * Constructs a {@link WiringReportCompiler} that writes its output to {@code outputPath}.
     *
     * @param outputPath the path to write the report to; must not be {@code null}
     */
    public WiringReportCompiler(final Path outputPath) {
        this.outputPath = outputPath;
    }

    @Override
    public void compile(final JDKCodeModel target,
                        final CodeModel codeModel,
                        final TelemetryRecorder telemetryRecorder) {
        final var trait = target.getTrait(BindingGraphTrait.class).orElse(null);
        if (trait == null) {
            return;
        }
        writeReport(trait, this.outputPath);
        reportWarnings(trait, telemetryRecorder);
    }

    // -------------------------------------------------------------------------
    // Package-private shared API (used by InjectionContext.snapshot)
    // -------------------------------------------------------------------------

    /**
     * Generates and writes the wiring report for {@code trait} to {@code outputPath}.
     *
     * @param trait      the binding graph trait to report on
     * @param outputPath the path to write the report to
     * @throws UncheckedIOException if writing fails
     */
    static void writeReport(final BindingGraphTrait trait, final Path outputPath) {
        try {
            final var parent = outputPath.getParent();
            if (parent != null) {
                Files.createDirectories(parent);
            }
            Files.writeString(outputPath, generateMarkdown(trait));
        } catch (final IOException e) {
            throw new UncheckedIOException("Failed to write wiring report to " + outputPath, e);
        }
    }

    // -------------------------------------------------------------------------
    // Report generation
    // -------------------------------------------------------------------------

    private static String generateMarkdown(final BindingGraphTrait trait) {
        final var sb = new StringBuilder();
        sb.append("# Wiring Map\n\n");
        sb.append("_Generated by `WiringReportCompiler`._\n\n");

        final var bindings = trait.bindings().toList();

        if (bindings.isEmpty()) {
            sb.append("No bindings registered.\n");
            return sb.toString();
        }

        // Group by scope/kind
        final var singletons = bindings.stream()
            .filter(n -> n.scope() != null && n.scope().getSimpleName().equals("Singleton"))
            .toList();
        final var customScoped = bindings.stream()
            .filter(n -> n.scope() != null && !n.scope().getSimpleName().equals("Singleton"))
            .toList();
        final var multis = bindings.stream()
            .filter(n -> n.kind() == BindingKind.MULTI)
            .toList();
        final var prototypes = bindings.stream()
            .filter(n -> n.scope() == null && n.kind() != BindingKind.MULTI)
            .toList();

        appendBindingSection(sb, "Singletons", singletons, trait);
        appendBindingSection(sb, "Custom-Scoped", customScoped, trait);
        appendBindingSection(sb, "Multibindings", multis, trait);
        appendBindingSection(sb, "Prototype", prototypes, trait);

        // Scope violations
        final var violations = findScopeViolations(trait);
        if (!violations.isEmpty()) {
            sb.append("## Scope Violations\n\n");
            violations.forEach(v -> sb.append("- ").append(v).append("\n"));
            sb.append("\n");
        }

        // Unsatisfied dependencies
        final var unsatisfied = trait.unsatisfiedDependencies()
            .sorted(Comparator.comparing(u -> displayName(u.from())))
            .toList();
        if (!unsatisfied.isEmpty()) {
            sb.append("## Unsatisfied Dependencies\n\n");
            sb.append("_These bindings depend on types not registered as explicit bindings ");
            sb.append("(auto-resolved or untracked at snapshot time)._\n\n");
            unsatisfied.forEach(u -> sb.append("- `")
                .append(displayName(u.from()))
                .append("` → `")
                .append(u.edge().dependency().signature())
                .append("`\n"));
            sb.append("\n");
        }

        return sb.toString();
    }

    private static void appendBindingSection(final StringBuilder sb,
                                             final String title,
                                             final List<BindingNode> nodes,
                                             final BindingGraphTrait trait) {
        if (nodes.isEmpty()) {
            return;
        }
        sb.append("## ").append(title).append("\n\n");
        nodes.stream()
            .sorted(Comparator.comparing(WiringReportCompiler::displayName))
            .forEach(node -> {
                sb.append("- `").append(displayName(node)).append("`");
                if (node.scope() != null && !node.scope().getSimpleName().equals("Singleton")) {
                    sb.append(" (@").append(node.scope().getSimpleName()).append(")");
                }
                if (node.declaringModule() != null) {
                    sb.append(" [").append(node.declaringModule().getSimpleName()).append("]");
                }
                sb.append("\n");

                final var deps = trait.dependenciesOf(node).toList();
                if (!deps.isEmpty()) {
                    sb.append("  - _depends on:_ ");
                    sb.append(deps.stream()
                        .map(WiringReportCompiler::displayName)
                        .sorted()
                        .collect(Collectors.joining(", ")));
                    sb.append("\n");
                }

                final var dependents = trait.dependentsOf(node).toList();
                if (!dependents.isEmpty()) {
                    sb.append("  - _depended on by:_ ");
                    sb.append(dependents.stream()
                        .map(WiringReportCompiler::displayName)
                        .sorted()
                        .collect(Collectors.joining(", ")));
                    sb.append("\n");
                }
            });
        sb.append("\n");
    }

    private static List<String> findScopeViolations(final BindingGraphTrait trait) {
        return trait.bindings()
            .filter(from -> Singleton.class.equals(from.scope()))
            .flatMap(from -> trait.dependenciesOf(from)
                .filter(to -> to.scope() == null && to.kind() == BindingKind.CLASS)
                .map(to -> "`" + displayName(from) + "` (@Singleton) → `"
                    + displayName(to) + "` (prototype)"))
            .sorted()
            .toList();
    }

    private static void reportWarnings(final BindingGraphTrait trait,
                                       final TelemetryRecorder telemetry) {
        findScopeViolations(trait).forEach(v ->
            telemetry.warn("Scope violation: " + v));
        trait.unsatisfiedDependencies().forEach(u ->
            telemetry.warn("Unsatisfied dependency: `" + displayName(u.from())
                + "` requires `" + u.edge().dependency().signature()
                + "` which is not registered as an explicit binding"));
    }

    /**
     * Returns a human-readable display name for the given {@link BindingNode}.
     */
    static String displayName(final BindingNode node) {
        if (node.typeDescriptor() != null) {
            return node.typeDescriptor().typeName().canonicalName();
        }
        return switch (node.kind()) {
            case VALUE -> "(value)";
            case SUPPLIER -> "(supplier)";
            default -> "(unknown)";
        };
    }
}
